nome = "Guilherme Arthur de Carvalho"

# cada numero é uma letra da variavel - começa em G = 0 
print(nome[0])
print(nome[-2]) # anda para trás - h (carvalho)
print(nome[:9]) # printa até o numero 9
print(nome[10:]) # printa a partir do numero 10
print(nome[10:16]) # printa x até y
print(nome[10:16:2]) # printa x até y com intervalo 
print(nome[:]) # printa tudo
print(nome[::-1]) # printa tudo de trás pra frente e letras invertidas
